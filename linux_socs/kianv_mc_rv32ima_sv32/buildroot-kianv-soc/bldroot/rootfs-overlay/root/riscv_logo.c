    uint8_t red5   = (pixel16 >> 11) & 0x1F;  // Top 5 bits
    uint8_t green6 = (pixel16 >> 5)  & 0x3F;  // Middle 6 bits
    uint8_t blue5  = pixel16 & 0x1F;          // Bottom 5 bits

    // Scale values up to 8 bits
    *r = (red5   << 3) | (red5   >> 2);  // Replicate the top 3 bits of red5 to its bottom 3 bits
